package com.seckill.mall.common;

import lombok.Getter;

/**
 * 统一状态码枚举
 * <p>
 * 定义系统中所有业务状态码及其对应的提示信息。
 * 状态码按业务模块分段编排：
 * <ul>
 *   <li>200 - 操作成功</li>
 *   <li>500 - 通用操作失败</li>
 *   <li>401x - 认证/授权相关错误</li>
 *   <li>402x - 参数校验相关错误</li>
 *   <li>501x - 秒杀业务相关错误（用户端）</li>
 *   <li>5015~5022 - 秒杀商品管理相关错误（管理端）</li>
 *   <li>503x - 订单业务相关错误</li>
 * </ul>
 * </p>
 */
@Getter
public enum ResultCode {

    /** 操作成功 */
    SUCCESS(200, "操作成功"),

    /** 通用操作失败 */
    FAIL(500, "操作失败"),

    // ==================== 认证相关 401x ====================

    /** 未登录或Token已过期，需要重新登录 */
    UNAUTHORIZED(401, "未登录或Token已过期"),

    /** 权限不足，无法访问该资源 */
    FORBIDDEN(403, "权限不足"),

    /** 用户不存在（登录时用户名错误） */
    USER_NOT_FOUND(4011, "用户不存在"),

    /** 密码错误 */
    PASSWORD_ERROR(4012, "密码错误"),

    /** 用户名已被注册 */
    USER_EXISTS(4013, "用户名已存在"),

    /** JWT Token无效或已被篡改 */
    TOKEN_INVALID(4014, "Token无效"),

    // ==================== 参数相关 402x ====================

    /** 请求参数校验不通过 */
    PARAM_ERROR(4020, "参数错误"),

    // ==================== 秒杀业务相关 501x ====================

    /** 秒杀活动尚未开始 */
    SECKILL_NOT_START(5010, "秒杀未开始"),

    /** 秒杀活动已结束 */
    SECKILL_ENDED(5011, "秒杀已结束"),

    /** 秒杀商品库存不足，已售罄 */
    SECKILL_STOCK_EMPTY(5012, "库存不足"),

    /** 用户重复抢购同一秒杀商品 */
    SECKILL_REPEAT(5013, "请勿重复抢购"),

    /** 秒杀抢购失败（库存竞争失败等原因） */
    SECKILL_FAIL(5014, "抢购失败"),

    // ==================== 秒杀商品管理相关 5015~5022 ====================

    /** 秒杀商品不存在 */
    SECKILL_GOODS_NOT_FOUND(5015, "秒杀商品不存在"),

    /** 仅未开始的秒杀商品可修改 */
    SECKILL_GOODS_NOT_START(5016, "仅未开始的秒杀商品可修改"),

    /** 秒杀价格必须大于0且低于原价 */
    SECKILL_PRICE_INVALID(5017, "秒杀价格必须大于0且低于原价"),

    /** 秒杀库存必须大于0 */
    SECKILL_STOCK_INVALID(5018, "秒杀库存必须大于0"),

    /** 秒杀时间设置无效，开始时间必须在未来且早于结束时间 */
    SECKILL_TIME_INVALID(5019, "秒杀时间设置无效，开始时间必须在未来且早于结束时间"),

    /** 进行中的秒杀活动不允许删除 */
    SECKILL_GOODS_IN_PROGRESS(5020, "进行中的秒杀活动不允许删除"),

    /** 只能增加库存，不能减少 */
    SECKILL_GOODS_STOCK_INVALID(5021, "只能增加库存，不能减少"),

    /** 商品已经在秒杀活动中存在 */
    SECKILL_GOODS_EXIST(5022, "商品已经在秒杀活动中存在"),

    // ==================== 秒杀活动状态管理相关 5023~5025 ====================

    /** 秒杀活动状态变更不合法（如不允许的状态跳转） */
    SECKILL_STATUS_INVALID(5023, "秒杀活动状态变更不合法"),

    /** 秒杀活动已结束，无法再次开始或者修改 */
    SECKILL_ALREADY_ENDED(5024, "秒杀活动已结束，无法再次开始或者修改"),

    /** 旧密码验证失败 */
    OLD_PASSWORD_ERROR(5025, "旧密码验证失败"),

    // ==================== 订单相关 503x ====================

    /** 订单不存在（查询时订单号错误） */
    ORDER_NOT_FOUND(5030, "订单不存在"),

    /** 订单已支付，不可重复支付 */
    ORDER_PAID(5031, "订单已支付"),

    /** 订单已取消 */
    ORDER_CANCELLED(5032, "订单已取消"),

    // ==================== 用户管理相关 504x ====================

    /** 用户已被禁用 */
    USER_DISABLED(5040, "用户已被禁用");

    /** 状态码 */
    private final int code;

    /** 提示信息 */
    private final String msg;

    ResultCode(int code, String msg) {
        this.code = code;
        this.msg = msg;
    }
}
